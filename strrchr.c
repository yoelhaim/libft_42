char	*ft_strrchr(const char *s, int c)
{
	int	i;
	char *last;

	i = 0;
	while (s[i])
	{
		if(s[i] == c)
			last = (char*)s + i;
		i++;
	}
	if(s[i] == '\0')
	{
		return ((char*)last);
	}
	return (0);
}
