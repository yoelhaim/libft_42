char	 *ft_strchr(const char *s, int c)
{
	int	i;
	int j;

	j = 0;
	i = 0;

	while(s[i])
	{
		if(s[i] == c)
		{
			return((char*)s + i);
		}
		i++;
	}
	return(0);
}
