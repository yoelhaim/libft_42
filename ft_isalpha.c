#include "libft.h"

size_t	ft_isalpha(char *c)
{
	size_t	i;

	i = 0;
	while (c[i])
	{
		if ((c[i] < 'a' || c[i] > 'z') && (c[i] < 'A' || c[i] > 'Z'))
			return (0);
		i++;
	}
	return (1);
}
